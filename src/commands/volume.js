const { SlashCommandBuilder, EmbedBuilder, MessageFlags } = require('discord.js');
const { DisTubeError } = require('distube');

module.exports = {
  data: new SlashCommandBuilder()
    .setName('volume')
    // DESCRI√á√ÉO TRADUZIDA
    .setDescription('Define o volume da reprodu√ß√£o de m√∫sica.')
    .addIntegerOption(option =>
      option.setName('volume')
        // DESCRI√á√ÉO DA OP√á√ÉO TRADUZIDA
        .setDescription('N√≠vel do volume (0-100).')
        .setRequired(true)),

  async execute(interaction) {
    const volume = interaction.options.getInteger('volume');
    const channel = interaction.member.voice.channel;

    if (!channel) {
      const embed = new EmbedBuilder()
        .setColor('#FF0000')
        // T√çTULO E DESCRI√á√ÉO TRADUZIDOS
        .setTitle('Erro')
        .setDescription('Voc√™ precisa estar em um canal de voz para definir o volume.');
      
      // Resposta de erro ef√™mera
      return interaction.reply({ embeds: [embed], flags: MessageFlags.Ephemeral });
    }

    // A verifica√ß√£o de volume deve ser ef√™mera
    if (volume < 0 || volume > 100) {
      const embed = new EmbedBuilder()
        .setColor('#FF9900')
        // T√çTULO E DESCRI√á√ÉO TRADUZIDOS
        .setTitle('Volume Inv√°lido')
        .setDescription('O n√≠vel do volume deve ser um n√∫mero entre 0 e 100.');
      
      return interaction.reply({ embeds: [embed], flags: MessageFlags.Ephemeral });
    }

    try {
      // √â preciso verificar se a fila existe antes de tentar alterar o volume
      const queue = interaction.client.playerManager.distube.getQueue(channel);
      if (!queue) {
          const embed = new EmbedBuilder()
              .setColor('#FF9900')
              .setTitle('Fila Inexistente')
              .setDescription('N√£o h√° nenhuma m√∫sica tocando no momento para ajustar o volume.');

          return interaction.reply({ embeds: [embed], flags: MessageFlags.Ephemeral });
      }

      await interaction.client.playerManager.distube.setVolume(channel, volume);

      const embed = new EmbedBuilder()
        .setColor('#00FF00')
        // T√çTULO E DESCRI√á√ÉO TRADUZIDOS
        .setTitle('Volume Definido')
        .setDescription(`üîä Volume alterado para **${volume}%**.`);
      
      // Resposta de confirma√ß√£o P√öBLICA
      await interaction.reply({ embeds: [embed] });

    } catch (error) {
      console.error('Set Volume Error:', error);

      const embed = new EmbedBuilder()
        .setColor('#FF0000')
        // T√çTULO E DESCRI√á√ÉO TRADUZIDOS
        .setTitle('Erro')
        .setDescription('Ocorreu um erro ao tentar definir o volume.');
      
      // Resposta de erro ef√™mera
      await interaction.reply({ embeds: [embed], flags: MessageFlags.Ephemeral });
    }
  },
};